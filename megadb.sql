CREATE PROCEDURE spVote
 	@title VARCHAR(50)
AS
	UPDATE GameDetail
	SET popularity = popularity + 1
	FROM GameDetail
	WHERE name = @title

	SELECT popularity
	FROM GameDetail
	WHERE name = @title
